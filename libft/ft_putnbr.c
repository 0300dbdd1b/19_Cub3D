#include "libft.h"

void	ft_putnbr(int n)
{
	char	res;
	int		sign;

	sign = 1;
	if (n < 0)
	{
		write(1, "-", 1);
		sign *= -1;
	}
	if (n >= 10 || n <= -10)
		ft_putnbr((n / 10) * sign);
	res = (char)(((n % 10) * sign) + 48);
	write(1, &res, 1);
}
