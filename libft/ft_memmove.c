#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t n)
{
	size_t				i;

	if (n == 0 || (!dst && !src))
		return (dst);
	if (dst < src)
	{
		i = 0;
		while (i < n)
		{
			((char *)dst)[i] = ((char *)src)[i];
			i++;
		}
	}
	else if (dst > src)
	{
		while (n > 0)
		{
			((char *)dst)[n - 1] = ((char *)src)[n - 1];
			n--;
		}
	}
	return (dst);
}
